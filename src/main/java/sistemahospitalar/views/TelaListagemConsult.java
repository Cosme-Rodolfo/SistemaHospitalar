    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int id = Integer.parseInt(jTextFieldBuscarId.getText());
        // Cria uma instância Consulta
        
        ConsultaMedica consultaEditar = GrupoConsulta.getGrupo().get(id);
        EditarConsulta editarConsulta = new EditarConsulta(consultaEditar);

        // Torna visível
        editarConsulta.setVisible(true);

        dispose();
        
    }//GEN-LAST:event_jButtonEditarActionPerformed
    private void exibirApenasLinhaComId(int ID) {
        DefaultTableModel model = (DefaultTableModel) jTableConsulta.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            int idLinha = (int) model.getValueAt(i, 0); 
            if (idLinha != ID) {
                // Remove a linha que não corresponde o nome do paciente pesquisado
                model.removeRow(i);
                i--; // Atualiza o índice após a remoção da linha
            }
        }
    }
    private void exibirTodasAsLinhas() {
        DefaultTableModel model = (DefaultTableModel) jTableConsulta.getModel();
        model.setRowCount(0); // remove os elementos filtrados para evitar duplicação 
        
        for (ConsultaMedica consulta : GrupoConsulta.getGrupo()){
            adicionarATabela(consulta); // preenche novamente a tabela com os todos os valores 
        }
    }
    private void jButtonFiltrarNomesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFiltrarNomesActionPerformed
        // Obtem o ID digitado
        String id = jTextFieldInserirNome.getText();
        exibirTodasAsLinhas();
        // Exibe apenas a linha que corresponde ao nome pesquisado
        if (!id.isEmpty()){
            exibirApenasLinhaComId(Integer.parseInt(id));
        }
        else {
            exibirTodasAsLinhas();
        }
    }//GEN-LAST:event_jButtonFiltrarNomesActionPerformed

