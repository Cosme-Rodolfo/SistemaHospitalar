    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        int id = Integer.parseInt(jTextFieldBuscarId.getText());
        
        GrupoConsulta.removerDoGrupo(id);
        for (int i = 0; i < GrupoConsulta.getGrupo().size(); i++){
            GrupoConsulta.getGrupo().get(i).setIdConsulta(i);
        }
        DefaultTableModel model = (DefaultTableModel) jTableConsulta.getModel();
        model.setRowCount(0);
        for (ConsultaMedica consulta : GrupoConsulta.getGrupo()){
            adicionarATabela(consulta);
        }
        
    }//GEN-LAST:event_jButtonDeletarActionPerformed

