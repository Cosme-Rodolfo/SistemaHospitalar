    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        int idPaciente = Integer.parseInt(jTextFieldBuscarId.getText());
        
        GrupoPaciente.removerDoGrupo(idPaciente);
        for (int i = 0; i < GrupoPaciente.getGrupo().size(); i++){
            GrupoPaciente.getGrupo().get(i).setIdPaciente(i);
        }
        DefaultTableModel model = (DefaultTableModel) jTablePaciente.getModel();
        model.setRowCount(0);
        for (Paciente paciente : GrupoPaciente.getGrupo()){
            adicionarPacienteATabela(paciente);
        }
        
    }//GEN-LAST:event_jButtonDeletarActionPerformed

