    private void exibirApenasLinhaComNome(String nomePaciente) {
        DefaultTableModel model = (DefaultTableModel) jTablePaciente.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            String nomeLinha = (String) model.getValueAt(i, 1); // 1 Ã© o Ã­ndice da coluna que contÃ©m o nome do paciente
            if (!nomeLinha.contains(nomePaciente)) {
                // Remove a linha que nÃ£o corresponde o nome do paciente pesquisado
                model.removeRow(i);
                i--; // Atualiza o Ã­ndice apÃ³s a remoÃ§Ã£o da linha
            }
        }
    }
    private void exibirTodasAsLinhas() {
        DefaultTableModel model = (DefaultTableModel) jTablePaciente.getModel();
        model.setRowCount(0); // remove os elementos filtrados para evitar duplicaÃ§Ã£o 
        
        for (Paciente paciente : GrupoPaciente.getGrupo()){
            adicionarPacienteATabela(paciente); // preenche novamente a tabela com os todos os valores 
        }
    }
    private void jButtonFiltrarNomesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFiltrarNomesActionPerformed
        // Obtem o ID do paciente digitado
        String nomePaciente = jTextFieldInserirNome.getText();
        exibirTodasAsLinhas();
        // Exibe apenas a linha que corresponde ao nome pesquisado
        System.out.println(nomePaciente);
        if (nomePaciente.length() > 1){
            exibirApenasLinhaComNome(nomePaciente);
        }
        else {
            exibirTodasAsLinhas();
        }
    }//GEN-LAST:event_jButtonFiltrarNomesActionPerformed

