    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // Obtem os dados da interface        
        if (jTextFieldExame.getText().isEmpty() || jTextFieldDiagnostico.getText().isEmpty() ||
            jTextFieldPrescricao.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos obrigatórios.");
            return;
        }

        String exameQueixa = jTextFieldExame.getText();
        String diagnostico = jTextFieldDiagnostico.getText();
        String prescricao = jTextFieldPrescricao.getText();
        boolean indicacaoCirurgia = jRadioButtonYES.isSelected();
        int idPaciente = Integer.parseInt(jComboBoxIdPaciente.getSelectedItem().toString());
        int idMedico = Integer.parseInt(jComboBoxIdMedico.getSelectedItem().toString());
        
        
        // Cria um novo objeto ConsultaMedica com os dados fornecidos
        ConsultaMedica novaConsulta = new ConsultaMedica(idPaciente, idMedico, exameQueixa, diagnostico, prescricao, indicacaoCirurgia);
        novaConsulta.setIdConsulta(minhaConsulta.getIdConsulta());
        GrupoConsulta.getGrupo().set(minhaConsulta.getIdConsulta(), novaConsulta);
        
        List<ConsultaMedica> consultas;
        consultas = GrupoPaciente.getGrupo().get(minhaConsulta.getIdPaciente()).getHistoricoConsultasMedica();
        
        for (int i = 0; i < consultas.size(); i++ ){
            if (consultas.get(i).getIdConsulta() == minhaConsulta.getIdConsulta()){
                consultas.set(i, novaConsulta);
            }
        }
        //GrupoPaciente.getGrupo().get(minhaConsulta.getIdPaciente()).setHistoricoConsultasMedica(consultas);
        try {
             GrupoPaciente.getGrupo().get(minhaConsulta.getIdPaciente()).setHistoricoConsultasMedica(consultas);
            // Exibe a mensagem de sucesso
            JOptionPane.showMessageDialog(null, "Paciente cadastrado com sucesso!");
        } catch (Exception ex) {
            // Se ocorrer uma exceção, exibe uma mensagem de erro
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar o paciente: " + ex.getMessage());
        }

    }//GEN-LAST:event_jButtonSalvarActionPerformed

