    public EditarConsulta(ConsultaMedica cons) {
        
        initComponents();
        buttonGroup1.add(jRadioButtonYES);
        buttonGroup1.add(jRadioButtonNO);
        // Desabilita a edição dos campos de idade e ID do paciente
        minhaConsulta = cons;
        
        jComboBoxIdMedico.removeAllItems();
        jComboBoxIdPaciente.removeAllItems();
        for (Medico medico : GrupoMedico.getGrupo()){
            jComboBoxIdMedico.addItem(String.valueOf(medico.getIdMedico()));
        }
        for (Paciente paciente : GrupoPaciente.getGrupo()) {
            jComboBoxIdPaciente.addItem(String.valueOf(paciente.getIdPaciente()));
        }
        
        jComboBoxIdMedico.setSelectedIndex(minhaConsulta.getIdMedico());
        jComboBoxIdPaciente.setSelectedIndex(minhaConsulta.getIdPaciente());
        
        jTextFieldExame.setText(minhaConsulta.getExameQueixa());
        jTextFieldDiagnostico.setText(minhaConsulta.getDiagnostico());
        jTextFieldPrescricao.setText(minhaConsulta.getPrescricao());
        
        if (minhaConsulta.isIndicacaoCirurgia()) {
            jRadioButtonYES.setSelected(true);
        } else {
            jRadioButtonNO.setSelected(true);
        }
    }

