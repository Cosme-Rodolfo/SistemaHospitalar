    private void adicionarATabela(Enfermeiro enfermeiro) {
        DefaultTableModel model = (DefaultTableModel) jTableEnfermeiro.getModel();
        Object[] rowData;
        rowData = new Object[]{
        enfermeiro.getIdEnfermeiro(),
        enfermeiro.getNomeCompleto(),
        enfermeiro.getDataNascimento().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")),
        enfermeiro.getEndereco().getRua(),
        enfermeiro.getEndereco().getNumero(),
        enfermeiro.getEndereco().getBairro(),
        enfermeiro.getEndereco().getCidade(),
        enfermeiro.getEndereco().getEstado(),
        enfermeiro.getEndereco().getCep(),
        enfermeiro.getContato().getTelefone(),
        enfermeiro.getContato().getCelular(),
        enfermeiro.getContato().getEmail(),
        enfermeiro.getSetor(),
        enfermeiro.getChSemanais(),
        enfermeiro.getGenero().getDescricao(),
        enfermeiro.isTreinadoOpRx(),
        };
        model.addRow(rowData);  
    }
    
