    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        int id = Integer.parseInt(jTextFieldBuscarId.getText());
        
        GrupoEnfermeiro.removerDoGrupo(id);
        for (int i = 0; i < GrupoEnfermeiro.getGrupo().size(); i++){
            GrupoEnfermeiro.getGrupo().get(i).setIdEnfermeiro(i);
        }
        DefaultTableModel model = (DefaultTableModel) jTableEnfermeiro.getModel();
        model.setRowCount(0);
        for (Enfermeiro enfermeiro : GrupoEnfermeiro.getGrupo()){
            adicionarATabela(enfermeiro);
        }
        
    }//GEN-LAST:event_jButtonDeletarActionPerformed

