    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        // Obtem os dados da interface
        String nomeCompleto = jTextFieldNome.getText();
        LocalDate dataNascimento = null;
        try {
            dataNascimento = LocalDate.parse(jTextFieldDataNasc.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        } catch (DateTimeParseException e) {
            // Trataa o erro de formatação da data de nascimento
            JOptionPane.showMessageDialog(null, "Formato de data de nascimento inválido. Utilize o formato dd/MM/yyyy.");
            return; 
        }
        if (jTextFieldNome.getText().isEmpty() || jTextFieldDataNasc.getText().isEmpty() || jTextFieldRua.getText().isEmpty() ||
            jTextFieldNum.getText().isEmpty() || jTextFieldBairro.getText().isEmpty() || jTextFieldCidade.getText().isEmpty() ||
            jTextFieldEstado.getText().isEmpty() || jTextFieldCEP.getText().isEmpty() || jTextFieldTel.getText().isEmpty() ||
            jTextFieldCel.getText().isEmpty() || jTextFieldEmail.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos.");
            return;
        }
        
        String rua = jTextFieldRua.getText();
        int numero = Integer.parseInt(jTextFieldNum.getText());
        String bairro = jTextFieldBairro.getText();
        String cidade = jTextFieldCidade.getText();
        String estado = jTextFieldEstado.getText();
        int cep = Integer.parseInt(jTextFieldCEP.getText());
        String telefone = jTextFieldTel.getText();
        String celular = jTextFieldCel.getText();
        String email = jTextFieldEmail.getText();
        Date dataCadastro = new Date(); // Utiliza a data atual
        String obsGeral = jTextFieldObs.getText();
        Genero genero = obterGeneroSelecionado(); 

        // Cria um novo objeto Paciente com os dados fornecidos
        Endereco endereco = new Endereco(rua, numero, bairro, cidade, estado, cep);
        ContatoTelEmail contato = new ContatoTelEmail(telefone, celular, email);
        Paciente novoPaciente = new Paciente(nomeCompleto, dataNascimento, endereco, contato, genero, dataCadastro, obsGeral);
        novoPaciente.setResponsaveis(meuPaciente.getResponsaveis());
        
        try {
            // Adiciona o novo paciente ao grupo de pacientes
            GrupoPaciente.adicionarAoGrupo(novoPaciente);

        // Exibe a mensagem de sucesso
        JOptionPane.showMessageDialog(null, "Paciente cadastrado com sucesso!");
        } catch (Exception ex) {
            // Se ocorrer uma exceção, exibe uma mensagem de erro
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar o paciente: " + ex.getMessage());
    }

       
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

