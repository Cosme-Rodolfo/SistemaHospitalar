    private Genero obterGeneroSelecionado() {
        if (jRadioButtonF.isSelected()) {
            return Genero.F;
        } else if (jRadioButtonM.isSelected()) {
            return Genero.M;
        } else {
            return null; // Se nenhum gênero estiver selecionado
        }
    }
         
