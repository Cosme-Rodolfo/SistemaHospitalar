    public CadastroPaciente(Paciente pac) {
        
        initComponents();
        buttonGroup1.add(jRadioButtonF);
        buttonGroup1.add(jRadioButtonM);
        // Desabilita a edição dos campos de idade e ID do paciente
        meuPaciente = pac;
        
        jTextFieldNome.setText(meuPaciente.getNomeCompleto());        
        if (meuPaciente.getEndereco() != null){
            jTextFieldRua.setText(meuPaciente.getEndereco().getRua());
            jTextFieldNum.setText(String.valueOf(meuPaciente.getEndereco().getNumero()));
            jTextFieldBairro.setText(meuPaciente.getEndereco().getBairro());
            jTextFieldCidade.setText(meuPaciente.getEndereco().getCidade());
            jTextFieldEstado.setText(meuPaciente.getEndereco().getEstado());
            jTextFieldCEP.setText(String.valueOf(meuPaciente.getEndereco().getCep()));
        }
        if (meuPaciente.getContato() != null){
            jTextFieldTel.setText(meuPaciente.getContato().getTelefone());
            jTextFieldCel.setText(meuPaciente.getContato().getCelular());
            jTextFieldEmail.setText(meuPaciente.getContato().getEmail());
        }
        jTextFieldObs.setText(meuPaciente.getObsGeral());
        if (meuPaciente.getGenero() == Genero.F) {
            jRadioButtonF.setSelected(true);
        } else if (meuPaciente.getGenero() == Genero.M) {
            jRadioButtonM.setSelected(true);
        }
        
        for (Responsavel responsavel : meuPaciente.getResponsaveis())
        {
            adicionarResponsavelATabela(responsavel);
        }
    }
    
    private void adicionarResponsavelATabela(Responsavel reposavel) {
        DefaultTableModel model = (DefaultTableModel) jTableResps.getModel();
        Object[] rowData = new Object[]{
            reposavel.getIdResponsavel(),
            reposavel.getNomeResponsavel(),
            reposavel.getTelResponsavel(),
            reposavel.getCelResponsavel(),
            reposavel.getEmailResponsavel(),
        };
        model.addRow(rowData);   
    }

