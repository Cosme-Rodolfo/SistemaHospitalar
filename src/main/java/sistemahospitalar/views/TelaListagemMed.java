    private void adicionarATabela(Medico medico) {
        DefaultTableModel model = (DefaultTableModel) jTableMedico.getModel();
        Object[] rowData;
        rowData = new Object[]{
        medico.getIdMedico(),
        medico.getNomeCompleto(),
        medico.getDataNascimento().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")),
        medico.getEndereco().getRua(),
        medico.getEndereco().getNumero(),
        medico.getEndereco().getBairro(),
        medico.getEndereco().getCidade(),
        medico.getEndereco().getEstado(),
        medico.getEndereco().getCep(),
        medico.getContato().getTelefone(),
        medico.getContato().getCelular(),
        medico.getContato().getEmail(),
        medico.getNumeroCRM(),
        medico.isCirurgiao(),
        medico.getSetor(),
        medico.getChSemanais(),
        medico.getGenero().getDescricao(),
        String.join(", ", medico.getAreasEspecialidade()),
         };
        model.addRow(rowData);  
    }
    
