    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        int id = Integer.parseInt(jTextFieldBuscarId.getText());
        
        GrupoMedico.removerDoGrupo(id);
        for (int i = 0; i < GrupoMedico.getGrupo().size(); i++){
            GrupoMedico.getGrupo().get(i).setIdMedico(i);
        }
        DefaultTableModel model = (DefaultTableModel) jTableMedico.getModel();
        model.setRowCount(0);
        for (Medico medico : GrupoMedico.getGrupo()){
            adicionarATabela(medico);
        }
        
    }//GEN-LAST:event_jButtonDeletarActionPerformed

