    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int id = Integer.parseInt(jTextFieldBuscarId.getText());
        // Cria uma instância da TelaMedico
        
        Medico medicoEditar = GrupoMedico.getGrupo().get(id);
        EditarMedico editarMedico = new EditarMedico(medicoEditar);

        // Torna a TelaMedico visível
        editarMedico.setVisible(true);

        // Opcional: fecha a TelaPrincipal se desejado
        dispose();
        
    }//GEN-LAST:event_jButtonEditarActionPerformed
    private void exibirApenasLinhaComNome(String nome) {
        DefaultTableModel model = (DefaultTableModel) jTableMedico.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            String nomeLinha = (String) model.getValueAt(i, 1); // 1 é o índice da coluna que contém o nome do medico
            if (!nomeLinha.contains(nome)) {
                // Remove a linha que não corresponde o nome pesquisado
                model.removeRow(i);
                i--; // Atualiza o índice após a remoção da linha
            }
        }
    }
    private void exibirTodasAsLinhas() {
        DefaultTableModel model = (DefaultTableModel) jTableMedico.getModel();
        model.setRowCount(0); // remove os elementos filtrados para evitar duplicação 
        
        for (Medico medico : GrupoMedico.getGrupo()){
            adicionarATabela(medico); // preenche novamente a tabela com os todos os valores 
        }
    }
    private void jButtonFiltrarNomesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFiltrarNomesActionPerformed
        // Obtem o ID do paciente digitado
        String nome = jTextFieldInserirNome.getText();
        exibirTodasAsLinhas();
        // Exibe apenas a linha que corresponde ao nome pesquisado
        if (nome.length() > 1){
            exibirApenasLinhaComNome(nome);
        }
        else {
            exibirTodasAsLinhas();
        }
    }//GEN-LAST:event_jButtonFiltrarNomesActionPerformed

